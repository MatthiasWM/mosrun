/**
 * Return the name of the system variables.
 */
const char *gvarName(unsigned int id, const char ** description)
{
  if (description) *description = "";
  if (id>0x1e00) {
    return "NOT_A_LOCAL_VARIABLE";
  }
  GVars *t = gvarLUT;
  for (;;) {
    if (t->id == 0xffffffff)
      break;
    if (t->id == id) {
      if (description && t->comment)
        *description = t->comment;
      return t->name;
    }
    t++;
  }
  return "Unknown";
}
